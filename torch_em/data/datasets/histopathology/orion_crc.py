"""ORION-CRC contains paired H&E and multiplex immunofluorescence images of colorectal cancer tissue.

This loader supports the processed ORION-CRC tile dataset released with MIPHEI-ViT:
https://zenodo.org/records/15340874. The source ORION-CRC dataset is available from
https://zenodo.org/records/7637988 and is described in https://doi.org/10.1038/s43018-023-00576-1.

The processed release provides H&E tiles, mIF tiles, Cellpose-generated nucleus instance masks, and per-cell CSV
metadata with nucleus positions and cell types. This loader converts each tile into HDF5 with:

- `raw/he`: H&E tile.
- `raw/mif`: mIF tile.
- `labels/nucleus/instances`: Cellpose-generated nucleus instance segmentation mask.
- `labels/nucleus/semantic`: derived nucleus semantic segmentation mask, if cell-type CSV metadata is available.

The nucleus instance labels are generated by Cellpose on the DAPI channel in the processed ORION release, so they
are algorithm-generated instance segmentations, not manual annotations. The semantic labels are not native pixel
annotations either: they are derived by assigning the CSV cell type of each nucleus coordinate to the corresponding
nucleus instance region in `labels/nucleus/instances`. Background is 0 and class IDs are stored in
`semantic_label_mapping.csv` in the preprocessed output folder.

Please cite the ORION-CRC and MIPHEI-ViT publications if you use this dataset for your research.
"""

import os
import re
from glob import glob
from typing import List, Literal, Optional, Tuple, Union

import imageio.v3 as imageio
import numpy as np
import pandas as pd

from torch.utils.data import Dataset, DataLoader

import torch_em

from .. import util


URL = "https://zenodo.org/api/records/15340874/files/ORIONCRC_dataset_tile_20x.zip/content"
ZIP_NAME = "ORIONCRC_dataset_tile_20x.zip"
SPLITS = ("train", "val", "test")

CELL_TYPE_COLUMNS = ("cell_type", "celltype", "cell_type_pred", "predicted_cell_type", "phenotype", "class", "label")
X_COLUMNS = ("x", "X", "centroid_x", "nucleus_x", "nuclei_x", "center_x")
Y_COLUMNS = ("y", "Y", "centroid_y", "nucleus_y", "nuclei_y", "center_y")
TILE_X_COLUMNS = ("tile_x", "x_start", "xmin", "min_x", "left")
TILE_Y_COLUMNS = ("tile_y", "y_start", "ymin", "min_y", "top")


def _find_file(path, name):
    matches = glob(os.path.join(path, "**", name), recursive=True)
    if len(matches) == 1:
        return matches[0]
    if len(matches) > 1:
        return sorted(matches)[0]
    return None


def _resolve_path(root, metadata_path, value):
    value = str(value)
    candidates = [
        os.path.join(os.path.dirname(metadata_path), value),
        os.path.join(root, value),
        value,
    ]
    for candidate in candidates:
        if os.path.exists(candidate):
            return candidate
    return candidates[0]


def _find_column(columns, candidates):
    lower_to_column = {column.lower(): column for column in columns}
    for candidate in candidates:
        if candidate.lower() in lower_to_column:
            return lower_to_column[candidate.lower()]
    return None


def _get_metadata(root, split):
    metadata_path = _find_file(root, f"{split}_dataframe.csv")
    if metadata_path is None:
        raise RuntimeError(f"Could not find {split}_dataframe.csv in {root}.")

    metadata = pd.read_csv(metadata_path)
    return metadata_path, metadata


def _get_slide_csv_paths(root):
    slide_dataframe_path = _find_file(root, "slide_dataframe.csv")
    if slide_dataframe_path is None:
        return {}

    slide_dataframe = pd.read_csv(slide_dataframe_path)
    if "slide_name" not in slide_dataframe.columns or "nuclei_csv_path" not in slide_dataframe.columns:
        return {}

    return {
        row["slide_name"]: _resolve_path(root, slide_dataframe_path, row["nuclei_csv_path"])
        for _, row in slide_dataframe.iterrows()
    }


def _parse_tile_origin(path):
    stem = os.path.splitext(os.path.basename(path))[0]
    numbers = [int(number) for number in re.findall(r"\d+", stem)]
    if len(numbers) < 2:
        return None
    return numbers[-2], numbers[-1]


def _get_tile_origin(row, image_path):
    x_column = _find_column(row.index, TILE_X_COLUMNS)
    y_column = _find_column(row.index, TILE_Y_COLUMNS)
    if x_column is not None and y_column is not None:
        return int(row[x_column]), int(row[y_column])
    return _parse_tile_origin(image_path)


def _get_cell_type_mapping(csv_tables, cell_type_column):
    cell_types = set()
    for table in csv_tables.values():
        cell_types.update(str(value) for value in table[cell_type_column].dropna().unique())
    return {cell_type: label_id for label_id, cell_type in enumerate(sorted(cell_types), start=1)}


def _read_image(path):
    image = imageio.imread(path)
    if image.ndim == 3:
        image = image.transpose(2, 0, 1)
    return image


def _read_label(path):
    label = imageio.imread(path)
    if label.ndim == 3:
        label = label[..., 0]
    return label


def _collect_cell_tables(root):
    tables = {}
    for slide_name, csv_path in _get_slide_csv_paths(root).items():
        if os.path.exists(csv_path):
            tables[slide_name] = pd.read_csv(csv_path)
    return tables


def _infer_cell_columns(cell_tables):
    if not cell_tables:
        return None

    first_table = next(iter(cell_tables.values()))
    cell_type_column = _find_column(first_table.columns, CELL_TYPE_COLUMNS)
    x_column = _find_column(first_table.columns, X_COLUMNS)
    y_column = _find_column(first_table.columns, Y_COLUMNS)
    if cell_type_column is None or x_column is None or y_column is None:
        return None
    return cell_type_column, x_column, y_column


def _write_cell_type_mapping(output_root, mapping):
    mapping_path = os.path.join(output_root, "semantic_label_mapping.csv")
    if os.path.exists(mapping_path):
        return
    os.makedirs(output_root, exist_ok=True)
    pd.DataFrame(
        [{"label_id": label_id, "cell_type": cell_type} for cell_type, label_id in mapping.items()]
    ).to_csv(mapping_path, index=False)


def _make_semantic_label_from_instances(row, image_path, nuclei, cell_table, cell_type_mapping, cell_columns):
    cell_type_column, x_column, y_column = cell_columns
    origin = _get_tile_origin(row, image_path)
    tile_h, tile_w = nuclei.shape

    valid_mask = cell_table[cell_type_column].notna()
    if not valid_mask.any():
        return np.zeros(nuclei.shape, dtype="uint16")

    cells = cell_table[valid_mask]
    xs = cells[x_column].to_numpy(dtype=float)
    ys = cells[y_column].to_numpy(dtype=float)
    class_ids = np.array([cell_type_mapping[str(v)] for v in cells[cell_type_column]], dtype="uint16")

    if origin is not None:
        lx = np.round(xs - origin[0]).astype(int)
        ly = np.round(ys - origin[1]).astype(int)
        in_bounds = (lx >= 0) & (lx < tile_w) & (ly >= 0) & (ly < tile_h)
        inst_ids = np.zeros(len(xs), dtype=nuclei.dtype)
        inst_ids[in_bounds] = nuclei[ly[in_bounds], lx[in_bounds]]

        needs_fallback = ~in_bounds | (inst_ids == 0)
        if needs_fallback.any():
            lx_raw = np.round(xs).astype(int)
            ly_raw = np.round(ys).astype(int)
            fb = needs_fallback & (lx_raw >= 0) & (lx_raw < tile_w) & (ly_raw >= 0) & (ly_raw < tile_h)
            inst_ids[fb] = nuclei[ly_raw[fb], lx_raw[fb]]
    else:
        lx = np.round(xs).astype(int)
        ly = np.round(ys).astype(int)
        in_bounds = (lx >= 0) & (lx < tile_w) & (ly >= 0) & (ly < tile_h)
        inst_ids = np.zeros(len(xs), dtype=nuclei.dtype)
        inst_ids[in_bounds] = nuclei[ly[in_bounds], lx[in_bounds]]

    hit = inst_ids > 0
    if not hit.any():
        return np.zeros(nuclei.shape, dtype="uint16")

    inst_to_class = np.zeros(int(nuclei.max()) + 1, dtype="uint16")
    inst_to_class[inst_ids[hit]] = class_ids[hit]
    return inst_to_class[nuclei]


def _get_output_path(output_root, split, index, image_path):
    stem = os.path.splitext(os.path.basename(image_path))[0]
    split_root = os.path.join(output_root, split)
    os.makedirs(split_root, exist_ok=True)
    return os.path.join(split_root, f"{index:06}_{stem}.h5")


def _preprocess_split(root, split):
    import h5py

    metadata_path, metadata = _get_metadata(root, split)
    expected_columns = {"image_path", "target_path", "nuclei_path"}
    missing_columns = expected_columns - set(metadata.columns)
    if missing_columns:
        raise RuntimeError(f"Missing columns in {metadata_path}: {sorted(missing_columns)}.")

    output_root = os.path.join(root, "preprocessed", "orion_crc")
    cell_tables = _collect_cell_tables(root)
    cell_columns = _infer_cell_columns(cell_tables)
    cell_type_mapping = None
    if cell_columns is not None:
        cell_type_mapping = _get_cell_type_mapping(cell_tables, cell_columns[0])
        _write_cell_type_mapping(output_root, cell_type_mapping)

    for index, row in metadata.iterrows():
        he_path = _resolve_path(root, metadata_path, row["image_path"])
        mif_path = _resolve_path(root, metadata_path, row["target_path"])
        nucleus_path = _resolve_path(root, metadata_path, row["nuclei_path"])
        if not os.path.exists(he_path) or not os.path.exists(mif_path) or not os.path.exists(nucleus_path):
            continue

        output_path = _get_output_path(output_root, split, index, he_path)
        if os.path.exists(output_path):
            continue

        slide_name = row["slide_name"] if "slide_name" in row else None
        nuclei = _read_label(nucleus_path)
        with h5py.File(output_path, "w") as f:
            f.attrs["slide_name"] = slide_name or ""
            f.attrs["he_path"] = he_path
            f.attrs["mif_path"] = mif_path
            f.attrs["nuclei_path"] = nucleus_path
            f.create_dataset("raw/he", data=_read_image(he_path), compression="gzip")
            f.create_dataset("raw/mif", data=_read_image(mif_path), compression="gzip")
            f.create_dataset("labels/nucleus/instances", data=nuclei, compression="gzip")

            if cell_columns is not None and slide_name in cell_tables:
                semantics = _make_semantic_label_from_instances(
                    row, he_path, nuclei, cell_tables[slide_name], cell_type_mapping, cell_columns
                )
                f.create_dataset("labels/nucleus/semantic", data=semantics, compression="gzip")

    return output_root


def get_orion_crc_data(
    path: Union[os.PathLike, str],
    split: Optional[Literal["train", "val", "test"]] = None,
    download: bool = False,
) -> str:
    """Download and preprocess the processed ORION-CRC tile dataset.

    The archive is large, about 127 GB, so only use `download=True` when you really want to fetch it.
    Alternatively, download and extract `ORIONCRC_dataset_tile_20x.zip` manually into `path`.

    Args:
        path: Filepath to a folder where the downloaded data will be saved.
        split: The split to preprocess. By default all available splits are preprocessed.
        download: Whether to download the data if it is not present.

    Returns:
        Filepath where preprocessed HDF5 files are stored.
    """
    os.makedirs(path, exist_ok=True)
    if _find_file(path, "train_dataframe.csv") is None:
        zip_path = os.path.join(path, ZIP_NAME)
        if os.path.exists(zip_path):
            util.unzip(zip_path, path, remove=False)
        elif download:
            util.download_source(zip_path, URL, download=download, checksum=None)
            util.unzip(zip_path, path, remove=False)
        else:
            raise RuntimeError(
                f"Could not find the processed ORION-CRC data in {path}. "
                f"Please download {ZIP_NAME} from https://zenodo.org/records/15340874 and extract it there, "
                "or pass `download=True` to download the 127 GB archive."
            )

    splits = SPLITS if split is None else (split,)
    for this_split in splits:
        output_root = _preprocess_split(path, this_split)
    return output_root


def get_orion_crc_paths(
    path: Union[os.PathLike, str],
    split: Literal["train", "val", "test"],
    download: bool = False,
) -> List[str]:
    """Get paths to preprocessed ORION-CRC HDF5 tile files.

    Args:
        path: Filepath to a folder where the downloaded data will be saved.
        split: The split to use. Either "train", "val" or "test".
        download: Whether to download the data if it is not present.

    Returns:
        List of preprocessed HDF5 filepaths.
    """
    if split not in SPLITS:
        raise ValueError(f"'{split}' is not a valid split choice. Choose from {SPLITS}.")

    output_root = get_orion_crc_data(path, split=split, download=download)
    paths = sorted(glob(os.path.join(output_root, split, "*.h5")))
    if not paths:
        raise RuntimeError("Could not find any preprocessed ORION-CRC tiles for the requested settings.")
    return paths


def get_orion_crc_dataset(
    path: Union[os.PathLike, str],
    patch_shape: Tuple[int, int],
    split: Literal["train", "val", "test"],
    modality: Literal["he", "mif"] = "he",
    label_type: Literal["instances", "semantic"] = "instances",
    download: bool = False,
    resize_inputs: bool = False,
    **kwargs
) -> Dataset:
    """Get the processed ORION-CRC dataset for nucleus instance or semantic segmentation.

    Args:
        path: Filepath to a folder where the downloaded data will be saved.
        patch_shape: The patch shape to use for training.
        split: The split to use. Either "train", "val" or "test".
        modality: The raw modality to load. Either "he" for H&E or "mif" for multiplex immunofluorescence.
        label_type: The nucleus label target. Either "instances" or "semantic" for derived cell-type labels.
        download: Whether to download the data if it is not present.
        resize_inputs: Whether to resize the input images.
        kwargs: Additional keyword arguments for `torch_em.default_segmentation_dataset`.

    Returns:
        The segmentation dataset.
    """
    if modality not in ("he", "mif"):
        raise ValueError(f"'{modality}' is not a valid modality. Choose from 'he' or 'mif'.")
    if label_type not in ("instances", "semantic"):
        raise ValueError(f"'{label_type}' is not a valid label type. Choose from 'instances' or 'semantic'.")

    paths = get_orion_crc_paths(path, split, download)

    if label_type == "semantic":
        output_root = os.path.dirname(os.path.dirname(paths[0]))
        if not os.path.exists(os.path.join(output_root, "semantic_label_mapping.csv")):
            raise RuntimeError(
                "Semantic labels are not available for this ORION-CRC data. "
                "They require per-cell CSV metadata with cell types and nucleus coordinates."
            )

    if resize_inputs:
        resize_kwargs = {"patch_shape": patch_shape, "is_rgb": modality == "he"}
        kwargs, patch_shape = util.update_kwargs_for_resize_trafo(
            kwargs=kwargs, patch_shape=patch_shape, resize_inputs=resize_inputs, resize_kwargs=resize_kwargs
        )

    return torch_em.default_segmentation_dataset(
        raw_paths=paths,
        raw_key=f"raw/{modality}",
        label_paths=paths,
        label_key=f"labels/nucleus/{label_type}",
        is_seg_dataset=True,
        patch_shape=patch_shape,
        ndim=2,
        with_channels=True,
        **kwargs
    )


def get_orion_crc_loader(
    path: Union[os.PathLike, str],
    batch_size: int,
    patch_shape: Tuple[int, int],
    split: Literal["train", "val", "test"],
    modality: Literal["he", "mif"] = "he",
    label_type: Literal["instances", "semantic"] = "instances",
    download: bool = False,
    resize_inputs: bool = False,
    **kwargs
) -> DataLoader:
    """Get the processed ORION-CRC dataloader for nucleus instance or semantic segmentation.

    Args:
        path: Filepath to a folder where the downloaded data will be saved.
        batch_size: The batch size for training.
        patch_shape: The patch shape to use for training.
        split: The split to use. Either "train", "val" or "test".
        modality: The raw modality to load. Either "he" for H&E or "mif" for multiplex immunofluorescence.
        label_type: The nucleus label target. Either "instances" or "semantic" for derived cell-type labels.
        download: Whether to download the data if it is not present.
        resize_inputs: Whether to resize the input images.
        kwargs: Additional keyword arguments for `torch_em.default_segmentation_dataset` or for the PyTorch DataLoader.

    Returns:
        The DataLoader.
    """
    ds_kwargs, loader_kwargs = util.split_kwargs(torch_em.default_segmentation_dataset, **kwargs)
    dataset = get_orion_crc_dataset(
        path, patch_shape, split, modality=modality, label_type=label_type, download=download,
        resize_inputs=resize_inputs, **ds_kwargs
    )
    return torch_em.get_data_loader(dataset, batch_size, **loader_kwargs)
