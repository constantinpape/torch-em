"""The BBBC034v1 dataset contains 3D fluorescence microscopy images of human
induced pluripotent stem cell (iPSC) nuclei, with manually annotated instance
segmentation ground truth from the Allen Institute for Cell Science.

The dataset contains 3 imaging conditions (1024 x 1024 x 52 voxels):
- Single cell (AICS-12_134, 4 channels): has ground truth segmentation
- Colony center (3 channels, no ground truth)
- Colony edge (3 channels, no ground truth)

NOTE: The ground truth is sparsely annotated - only a subset of the visible
nuclei in the annotated volume are labeled. Consider using a sampler to reject
patches that contain no annotations when training.

Channels for the annotated single-cell image (AICS_12_134):
C=0 (CellMask plasma membrane), C=1 (GFP), C=2 (Hoechst/DNA nuclei), C=3 (Brightfield).

The dataset is located at https://bbbc.broadinstitute.org/BBBC034.
Generated by the AICS Microscopy & Image Analysis Team at the Allen Institute for Cell Science.
Please cite this dataset if you use it in your research.
"""

import os
from glob import glob
from natsort import natsorted
from typing import List, Tuple, Union

from torch.utils.data import Dataset, DataLoader

import torch_em

from .. import util


IMAGE_URL = "https://data.broadinstitute.org/bbbc/BBBC034/BBBC034_v1_dataset.zip"
IMAGE_CHECKSUM = None

GT_URL = "https://data.broadinstitute.org/bbbc/BBBC034/BBBC034_v1_DatasetGroundTruth.zip"
GT_CHECKSUM = None


def get_bbbc034_data(path: Union[os.PathLike, str], download: bool = False) -> str:
    """Download the BBBC034v1 dataset.

    Args:
        path: Filepath to a folder where the downloaded data will be saved.
        download: Whether to download the data if it is not present.

    Returns:
        The filepath to the extracted data directory.
    """
    data_dir = os.path.join(path, "BBBC034")
    if os.path.exists(data_dir):
        return data_dir

    os.makedirs(data_dir, exist_ok=True)

    img_zip = os.path.join(path, "BBBC034_v1_dataset.zip")
    gt_zip = os.path.join(path, "BBBC034_v1_DatasetGroundTruth.zip")

    util.download_source(img_zip, IMAGE_URL, download, checksum=IMAGE_CHECKSUM)
    util.download_source(gt_zip, GT_URL, download, checksum=GT_CHECKSUM)

    util.unzip(img_zip, data_dir)
    util.unzip(gt_zip, data_dir)

    return data_dir


def get_bbbc034_paths(
    path: Union[os.PathLike, str], channel: int = 2, download: bool = False,
) -> Tuple[List[str], List[str]]:
    """Get paths to the BBBC034v1 data.

    The ground truth segmentation covers a subset of the images. Raw images are
    single-channel TIFFs extracted from the multi-channel dataset.

    Args:
        path: Filepath to a folder where the downloaded data will be saved.
        channel: The imaging channel to use as raw input. Default: 2 (Hoechst/DNA nuclei).
            Available channels: 0=CellMask, 1=GFP, 2=Hoechst/DNA, 3=Brightfield.
        download: Whether to download the data if it is not present.

    Returns:
        List of filepaths for the image data.
        List of filepaths for the label data.
    """
    data_dir = get_bbbc034_data(path, download)

    raw_paths = natsorted(glob(os.path.join(data_dir, f"*_C={channel}.tif")))
    label_path = os.path.join(data_dir, "ground_truth_segmented.tif")

    if len(raw_paths) == 0:
        raise RuntimeError(
            f"No image files found for channel {channel} in {data_dir}. "
            "Please check the dataset structure after downloading."
        )
    if not os.path.exists(label_path):
        raise RuntimeError(
            f"Ground truth file not found: {label_path}. Please check the dataset structure after downloading."
        )

    return raw_paths, [label_path]


def get_bbbc034_dataset(
    path: Union[os.PathLike, str], patch_shape: Tuple[int, ...], channel: int = 2, download: bool = False, **kwargs,
) -> Dataset:
    """Get the BBBC034v1 dataset for 3D nucleus instance segmentation.

    Args:
        path: Filepath to a folder where the downloaded data will be saved.
        patch_shape: The patch shape to use for training.
        channel: The imaging channel to use as raw input. Default: 2 (Hoechst/DNA nuclei).
        download: Whether to download the data if it is not present.
        kwargs: Additional keyword arguments for `torch_em.default_segmentation_dataset`.

    Returns:
        The segmentation dataset.
    """
    raw_paths, label_paths = get_bbbc034_paths(path, channel, download)

    return torch_em.default_segmentation_dataset(
        raw_paths=raw_paths,
        raw_key=None,
        label_paths=label_paths,
        label_key=None,
        patch_shape=patch_shape,
        **kwargs,
    )


def get_bbbc034_loader(
    path: Union[os.PathLike, str],
    batch_size: int,
    patch_shape: Tuple[int, ...],
    channel: int = 2,
    download: bool = False,
    **kwargs,
) -> DataLoader:
    """Get the BBBC034v1 dataloader for 3D nucleus instance segmentation.

    Args:
        path: Filepath to a folder where the downloaded data will be saved.
        batch_size: The batch size for training.
        patch_shape: The patch shape to use for training.
        channel: The imaging channel to use as raw input. Default: 2 (Hoechst/DNA nuclei).
        download: Whether to download the data if it is not present.
        kwargs: Additional keyword arguments for `torch_em.default_segmentation_dataset` or for the PyTorch DataLoader.

    Returns:
        The DataLoader.
    """
    ds_kwargs, loader_kwargs = util.split_kwargs(torch_em.default_segmentation_dataset, **kwargs)
    dataset = get_bbbc034_dataset(path, patch_shape, channel, download, **ds_kwargs)
    return torch_em.get_data_loader(dataset, batch_size, **loader_kwargs)
